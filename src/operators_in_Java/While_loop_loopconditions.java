package operators_in_Java;

public class While_loop_loopconditions {

	public static void main(String[] args) {
		//program to display 1 to 5 using while loop
		int n=1;
		while (n<=5) {
			System.out.println(n);
			n++;
			
	}
		System.out.println("demo for loop");
		
		//program to display from 5 to 1 using while loop
		int N=5;
		while (N>=1) {
			System.out.println(N);
			N--;
		}
		System.out.println("demo for while loop");
		
	}

	}
